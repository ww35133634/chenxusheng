def say():
    """打印hello 武汉加油"""
    print("hello 武汉加油")


def sum_1_100():
    """完成1到100的累加求和"""
    i = 1
    sum = 0
    while i <=100:
        sum = sum + 1
        i += 1
    print("1到100的求和结果是:%d" %sum)

# 按住ctrl 键
say()
sum_1_100()









































