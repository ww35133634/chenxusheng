# 加强小案例,,,
# 要求:输入一个数字,,判断他是否是偶数,,并且将结果格式化输出
a = int(input('请输入一个数字:'))
if (a % 2) == 0:  # 2取余为0 那么就是整数
    print('%s是一个偶数' % a)
else:  # 代表他是一个奇数
    print('%s是一个奇数' % a)



















































































