"""
	语法格式一:               单一情况
		if 条件:
		____执行代码
		        ..........
"""

# 单一情况的判断
print('程序开始')
age = 18
# 如果这个人的年纪大于20岁,他就可以去当兵  True
if age >= 20:  # True 那么就 可以进去内部代码去执行 为False就跳过
    print('可以去当兵')  # 没有被执行


print('程序结束')























































































