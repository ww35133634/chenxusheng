# 判断两者之间的关系 逻辑的结果 True  False

#  1 2 3 4 5
# and  1 and 3 都是整型 >>> 两个条件必须都满足 结果才为True
# and  0.5 and 3 都是整型 >>> False True  >>> False

# or  1 or 3 都是整型 >>> True True  >>>> True 条件没有那么的苛刻
# or  0.5 or 3 都是整型 >>> False True >>> True

# not 取反
# 3 not 整型 >>>  True not >>> False
# 0.5 not 整型>>> False  not >>> True























































































