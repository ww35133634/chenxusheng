"""
演示while循环
"""

# while 条件(True False):
#     代码片段

# 计划跑10个圈
# 要执行十次  执行完十次之后就要停止 条件
# 12345678910 变量
# i 的 值永远 等于1 也就永远 符合进入循环的条件

print('程序开始')

i = 1  # 开始循环的代码
while i <= 5:  # True  11 False
    print('跑了一个圈')
    # 停止的标志就是十个圈
    i = i + 1  # i += 1

print('程序结束')


