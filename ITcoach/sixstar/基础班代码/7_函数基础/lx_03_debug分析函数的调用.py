"""
演示函数的调用分析
"""

print('程序开始执行了......')
def demo():
    print('1...')
    print('2...')
    print('3...')
    print('函数执行完了一遍')
demo()
print('函数第一遍调用完了')
print('*' * 50)
demo()
print('函数第二遍调用完了')
print('*' * 50)
demo()
print('函数第三遍调用完了')
print('*' * 50)
print('程序执行结束了')





























































