"""
演示函数的定义与调用格式一
"""

# 1.定义函数 解释性的编程语言  自上而下一行一行去执行,
# 见名知意的效果
def say():
    # 属于是函数内部,函数内部的代码就是一个整体,
    print('hello, 武汉加油')
    print('hello, 热干面加油')
    print('hello, 周黑鸭加油')

# 2.调用函数
say()
# 复用性很强,  你再次先完成这些功能,, 不需要再一次的写功能代码,, 直接再次调用就可以了
say()

say()






















































