"""
原生字符串r的作用
"""

import re

s = 'demo1\demo2\demo3'

#                   用\转义
data = re.match(r'demo1\\demo2\\demo3',s)
print(data.group())


data = re.match('demo1\\\demo2\\\demo3',s)
print(data.group())



